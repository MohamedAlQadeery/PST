    {
        parent::__construct($model);
    }

    public function index()
    {
        $transactions = Transaction::with(['provider', 'shop'])->get();

        return view('back.transaction.index')->with([
            'page_name' => parent::getPluralModelName(),
            'transactions' => $transactions,
        ]);
    }

